    private void MostrarRecorridosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarRecorridosActionPerformed
        String opcion = (String) JOptionPane.showInputDialog(null, "Â¿QUE RECORRIDO DESEA HACER?", "RECORRIDOS", JOptionPane.INFORMATION_MESSAGE,
                null, new Object[]{"PREORDEN", "POSTORDEN", "INORDEN", "TODOS"}, null);

        if (opcion == null) return;
        
        ModificacionesJframe mode = new ModificacionesJframe(arbolito); //Tengo otra clase que involucra a este objeto
        switch (opcion) {
            case "PREORDEN":
                jTextArea2.setText(mode.obtenerPreorden());
                break;
            case "POSTORDEN":
                jTextArea2.setText(mode.obtenerPostorden());
                break;
            case "INORDEN":
                jTextArea2.setText(mode.obtenerInorden());
                break;
            case "TODOS":
                jTextArea2.setText(mode.obtenerPreorden() + "\n" + mode.obtenerPostorden() + "\n" + mode.obtenerInorden());
                break;
        }
    }//GEN-LAST:event_MostrarRecorridosActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        String d = CampoEliminar.getText().trim().toUpperCase();
            try {
                if (d.isEmpty()) throw new Exception("El campo esta vacio");
                if (d.length() != 1 || !Character.isLetter(d.charAt(0))) 
                    throw new Exception ("Ingrese solo 1 caracter y ademas que no sea de tipo numerico");

                arbolito.eliminar(d.charAt(0));
                panelarbolito.repaint();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ERROR AL ELIMINAR: " + e.getMessage(),
                        "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        CampoEliminar.setText("");
    }//GEN-LAST:event_EliminarActionPerformed

