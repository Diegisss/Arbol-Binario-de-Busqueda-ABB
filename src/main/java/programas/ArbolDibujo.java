    private void InsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertarActionPerformed
        String d = CampoInsertar.getText().trim().toUpperCase();
        try {
                if (d.isEmpty()) throw new Exception("El campo esta vacio");
                if (d.length() != 1 || !Character.isLetter(d.charAt(0))) 
                    throw new Exception ("Solo se permite una letra (Nada de numeros)");

                arbolito.insertar(d.charAt(0));
                panelarbolito.repaint();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ERROR AL INSERTAR: " + e.getMessage(),
                        "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        CampoInsertar.setText("");
    }//GEN-LAST:event_InsertarActionPerformed

